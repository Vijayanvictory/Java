class Explicit{
	public static void main(String args[])
	{

	
	int a = 100;
	System.out.println(a);
	
	//byte value
	byte b = (byte)128;
	System.out.println(b);

	//float
	float f = 98.9f;
	System.out.println(f);
		
	//converting float to int
	int c = (int)f;
	System.out.println(c);
	}
}